    public static String uniqueMap(String str) {
        
        List<String> temp = new ArrayList<String>(Arrays.asList(str.split("\\s*,\\s*")));
        List<Integer> newTemp = temp.stream().map(s -> Integer.parseInt(s)).collect(Collectors.toList());
        
        String finalMap = "";
    
        for (int i = 0; i < newTemp.size(); i++) {
            int curr = newTemp.get(i);
            if (curr != 0) {
                finalMap = finalMap + "put(" + (i) + ", " + curr + ");" + System.lineSeparator();  
            }       
        }

        return finalMap;
    }

    /** 
    [flushMap str idx] is a naive implementation to return all possible pairs of flush combos
    with their respective hand values where 1 is A high royal all the way down to 7462. Flushes
    are in the range of 1-10 (str8 flush) and 323-1599 
    */
    public void flushMap(String str, int idx) {
    
        List<String> temp = Arrays.asList(str.split("\\s*,\\s*"));
        List<Integer> newTemp = temp.stream().map(s -> Integer.parseInt(s)).collect(Collectors.toList());
        
        for (int i = 0; i < newTemp.size(); i++) {
            int curr = newTemp.get(i);
            if (curr != 0) {
                System.out.print("(" + (i+idx) + ", " + curr + ")");  
                System.out.print(", ");
            }       
        }
    }
