    /**
    [binaryTableString file] is a string representation of all prime products and corresponding values
    of valid 5 card Texas Holdem hands read from [file] besides flushes, straights, and high cards,
    where each card is represented by some prime number. 
    */
    public static String binaryTableString(String file) {

        Path productsPath = Paths.get(file); //Name of file can be made const
        String productsStr = "";
        try {
            productsStr = new String(Files.readAllBytes(productsPath));
        }   
        catch(IOException e) {
            e.printStackTrace();
        }
        return productsStr;
    }

    /**
    [binaryTableArray str] is an int ArrayList of all prime products and corresponding values
    of valid 5 card Texas Holdem hands casted from [str] besides flushes, straights, and high cards,
    where each card is represented by some prime number. 
    */  
    public static List<Integer> binaryTableArray(String str) {
        List<String> productsStrArr = new ArrayList<String>(Arrays.asList(str.split("\\s*,\\s*")));
        List<Integer> productsFinal = new ArrayList<Integer>();
        
        for(String prodStr : productsStrArr) {
            try {
                productsFinal.add(Integer.parseInt(prodStr));
            } catch(NumberFormatException nfe) {
                nfe.printStackTrace();
            }
        }
        return productsFinal;
    }

